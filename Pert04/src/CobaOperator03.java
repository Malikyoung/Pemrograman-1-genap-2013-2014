// Latihan Variabel
package Latihan01.sesi1.bin;

public class CobaOperator03{
	
	public static void main(String[] Malik){
		int c=3;
		System.out.println("Nilai awal c adalah " + c);
		System.out.println("Nilai c=c+7 adalah " +(c+=7));
		System.out.println();
		
		int d=5;
		System.out.println("Nilai awal d adalah " + d);
		System.out.println("Nilai d=d-4 adalah " +(d-=4));
		System.out.println();
		
		int e=4;
		System.out.println("Nilai awal e adalah " + e);
		System.out.println("Nilai e=e*5 adalah " +(e*=5));
		System.out.println();
		
		int f=6;
		System.out.println("Nilai awal f adalah " + f);
		System.out.println("Nilai f=f/5 adalah " +(f/=5));
		System.out.println();
		
		int g=12;
		System.out.println("Nilai awal g adalah " + g);
		System.out.println("Nilai g=g%7 adalah " +(g%=7));
		System.out.println();
	}	
}